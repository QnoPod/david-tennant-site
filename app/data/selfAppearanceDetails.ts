export type SelfAppearanceDetail = {
  /** WORKS詳細で「本人」の下に表示する出演形態です。 */
  appearanceNote: string;
  /** 番組内で何をしたかが分かる、作品別の説明です。 */
  description: string;
};

type SelfAppearanceEntry = SelfAppearanceDetail & { titles: string[] };

/** 表記ゆれを無視してTMDBの原題・邦題と照合します。 */
function normalizeTitle(value: string) {
  return value.normalize("NFKC").toLowerCase().replace(/[\s・=‐‑‒–—\-.,:;!?"'()[\]{}~～＆&]/g, "");
}

