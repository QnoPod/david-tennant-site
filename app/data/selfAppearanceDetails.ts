const SELF_APPEARANCE_BY_TITLE = new Map(
  SELF_APPEARANCE_ENTRIES.flatMap((entry) => entry.titles.map((title) => [normalizeTitle(title), entry] as const)),
);

/**
 * 作品別辞書を優先し、未登録作品はTMDBの役名から出演形態を補います。
 * これにより、新しいSelfクレジットも共通の一文だけになりません。
 */
export function getSelfAppearanceDetail(titles: string[], rawRole = "Self"): SelfAppearanceDetail {
  const registered = titles
    .map((title) => SELF_APPEARANCE_BY_TITLE.get(normalizeTitle(title)))
    .find((entry): entry is SelfAppearanceEntry => Boolean(entry));
  if (registered) return registered;

  const title = titles.find(Boolean)?.trim() || "この番組";
  const role = rawRole.normalize("NFKC").toLowerCase();
  if (/archive footage|archival/.test(role)) {
    return {
      appearanceNote: "過去映像・アーカイブ出演",
      description: `『${title}』のために撮影された新規出演ではなく、デイヴィッド・テナント本人の過去のインタビュー、授賞式、イベントなどの記録映像が使用されている。映像の引用による登場のため、新しい演技や発言はない。通常の本人ゲストと区別できるよう、アーカイブ出演として表示している。`,
    };
  }
  if (/host|presenter|guest host/.test(role)) {
    return {
      appearanceNote: "司会・プレゼンター",
      description: `デイヴィッド・テナント本人が『${title}』の司会またはプレゼンターを担当。出演者や企画を紹介し、トーク、コーナー、番組全体の流れを進行する。俳優として架空の人物を演じる出演ではなく、視聴者と出演者をつなぐ案内役としての出演。`,
    };
  }
  if (/reader|reading/.test(role)) {
    return {
      appearanceNote: "朗読者",
      description: `デイヴィッド・テナント本人が『${title}』に朗読者として出演。俳優としての声、間、抑揚を用いて、対象となる文章や作品の意味と感情を伝える。固定の架空キャラクターを演じる出演ではなく、原文を観客へ届ける朗読パフォーマンス。`,
    };
  }
  if (/voice/.test(role)) {
    return {
      appearanceNote: "本人役（声）",
      description: `デイヴィッド・テナントが『${title}』に本人役の声で出演。実写で登場するのではなく、番組内に描かれた自身のアニメーション・キャラクターへ声を当てている。別名の架空人物や通常のナレーターではなく、本人像を使った声のカメオ出演。`,
    };
  }
  return {
    appearanceNote: role.includes("guest") ? "本人ゲスト" : "本人出演",
    description: `デイヴィッド・テナントが『${title}』に本人として出演。ドラマ上の別人物を演じる出演ではなく、自身の活動や出演作について語り、番組内のトーク、取材、企画に参加する。`,
  };
}
